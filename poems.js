var poems = {
    "A Mechanic's Apostrophe to Nature" :
    [`If I could fix a broken heart
    with just a hammer and a wrench
    and my box of unused parts,
    then I would wait upon this bench
    for you to see that I am here,
    as I will always be, my dear.
    
    If I could set truth in a ring,
    all days, but nightless, would I spend
    to make my tongs and hammers sing
    until I reach the end of end-
    less loops with gold and silver laid
    in love unbound that never fades.
    
    If I had a chest of all my faults,
    the only one of iron weight,
    the one that I wish I could halt,
    is from those nights when I spurned fate
    and said that we could never be,
    that separate is our destiny.
    
    If I could tell you but a phrase,
    I’d search the air and sea below,
    I’d comb the woods for many days,
    I’d ask the minnow and the crow,
    all to find what I could say
    to make you see that I will stay.
    
    If I could see you one last time,
    before you fade out of my life,
    I’d hand to you a sheaf of thyme
    and pray that I could end our strife.
    But then my phone would ring, and I’d
    kill you, and wish you hadn’t died.`,
    "Spencer Hill",
    "The Nature Conservancy"]
}

export function getPoem(name) {
    return poems[name]
}